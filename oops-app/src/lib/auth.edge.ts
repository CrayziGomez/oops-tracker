import NextAuth from "next-auth";
import Credentials from "next-auth/providers/credentials";

// Lightweight auth config for middleware (no Prisma/bcrypt - those are Node.js only)
// The actual credential validation happens in the full auth.ts via the authorize callback
export const { auth: authMiddleware } = NextAuth({
  session: {
    strategy: "jwt",
  },
  pages: {
    signIn: "/login",
  },
  providers: [
    Credentials({
      name: "credentials",
      credentials: {
        email: { label: "Email", type: "email" },
        password: { label: "Password", type: "password" },
      },
      // This won't be called in middleware - only jwt/session callbacks matter
      authorize: () => null,
    }),
  ],
  callbacks: {
    async jwt({ token, user }) {
      if (user) {
        token.id = user.id;
        token.role = (user as { role: string }).role;
      }
      return token;
    },
    async session({ session, token }) {
      if (session.user) {
        session.user.id = token.id as string;
        (session.user as { role: string }).role = token.role as string;
      }
      return session;
    },
  },
});
